from pathlib import Path

def create_sample_txt():
    """创建示例txt文件作为知识库"""
    content = """桩基础工程知识库

第一章 桩基础基本概念
桩是用锤击、振动、射液、螺旋钻进或其他方法在地基中成孔，然后在孔形或孔内放入钢骨架，
浇灌混凝土或其他材料而成的深基础或加固地基的技术。

常见的桩类型有：预制桩、灌注桩、钢桩、木桩等。

第二章 桩基设计原则
1. 桩的承载力应大于上部结构传来的荷载
2. 桩的设置应充分考虑地质条件
3. 桩的布置应均匀分布，避免不均差沉降
4. 桩型的选择应根据现场条件、建筑要求、经济因素综合考虑

第三章 常见桩型
1. 沉管灌注桩：通过沉管设备将钢管压入地中，然后提管并灌混凝土成桩
2. 钻孔灌注桩：用钻孔机械成孔，然后灌混凝土成桩
3. 打入式预制桩：用锤击等方式直接打入地中的预制桩
4. 静压桩：用静液压力逐节下放预制桩至设计深度

第四章 桩基施工要点
1. 成孔工艺：需要根据土层情况选择合适的成孔方法
2. 钢筋笼制作：钢筋要满足设计要求，接头应按规范处理
3. 混凝土灌注：混凝土要满足强度等级要求，灌注过程要连续进行
4. 桩头处理：桩头要按要求进行清理和处理

第五章 桩基检验评估
1. 桩的承载力检验：通过静载试验确定桩的极限承载力
2. 桩身完整性检测：通过超声波等方法检测桩的完整性
3. 沉降观测：对桩基沉降进行长期观测
4. 质量评估：综合各项检测结果进行质量评估

第六章 特殊工况处理
1. 软土地基：采用长桩或增加桩数
2. 液化土：避免桩基在液化层以上
3. 岩溶地区：注意地下洞穴的处理
4. 冻胀土：考虑冻胀对桩的影响
"""
    
    knowledge_dir = Path("knowledge")
    knowledge_dir.mkdir(exist_ok=True)
    
    txt_file = knowledge_dir / "桩基础知识.txt"
    with open(txt_file, "w", encoding="utf-8") as f:
        f.write(content)
    
    print(f"Sample knowledge file created: {txt_file}")

if __name__ == "__main__":
    create_sample_txt()
